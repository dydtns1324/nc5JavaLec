class Jinsu {
	public static void main(String[] args){
		System.out.println("10진수 = " + 250);
		System.out.println("2진수 = " + Integer.toBinaryString(250) ); //11111010
		System.out.println("8진수 = " + Integer.toOctalString(250)); //372 
		System.out.println("16진수 = " + Integer.toHexString(250)); //fa
	
	}
}